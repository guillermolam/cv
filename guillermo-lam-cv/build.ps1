# From the root of your project

# 1. Install dependencies (do this once, or after you update package.json)
bun install

# 2. Start the dev server (hot reload, at http://localhost:5173 by default)
bun run dev

# 3. Build the production site (outputs to dist/)
bun run build

# 4. Preview the built site (serves dist/ on localhost)
bun run preview
